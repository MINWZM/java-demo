import org.junit.Test;

public class BitwiseOperatorsTest {
    @Test
    public void testLeft(){
        System.out.println(1<<1);
        System.out.println(1<<2);
        System.out.println(1<<3);
    }
    @Test
    public void testRight(){
        System.out.println(1>>1);
        System.out.println(1>>2);
        System.out.println(1>>3);
    }
    
}
